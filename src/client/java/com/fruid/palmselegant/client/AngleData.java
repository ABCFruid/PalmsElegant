package com.fruid.palmselegant.client;

public class AngleData {

    public String name;
    public float yaw;
    public float pitch;
    public int color;

    public AngleData(String name, float yaw, float pitch, int color) {
        this.name = name;
        this.yaw = yaw;
        this.pitch = pitch;
        this.color = color;
    }
}